function closeFacebookAnalysis__form(){
    var facebookAnalysis_form_ = document.getElementById('facebookAnalysis_form_');
    if(facebookAnalysis_form_.style.display === 'block')
    {
        facebookAnalysis_form_.style.display = 'none';
    }
    else
    {
        facebookAnalysis_form_.style.display = 'block';
    }
}